bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    tap_code(KC_INT7);
    return true;
}

uint16_t get_tapping_term(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        case LT(1, KC_TAB):
            return 200;
        case LT(2, KC_SPC):
            return 200;
        default:
            return TAPPING_TERM;
    }
}

